    /**Lobster**/

var lobster = document.getElementById("lobster");
var lobsterP = document.getElementById("lobster-p");

function showLobster() {
    lobsterP.innerHTML = "Lobster";
    lobsterP.style.fontSize = "60px";
    lobsterP.style.margin = "auto";
    lobster.style.backgroundColor = "pink";
};

function hideLobster() {
    lobsterP.innerHTML = "Life isn't finding shelter in the storm. It's about learning to dance in the rain.";
    lobsterP.style.fontSize = "20px";
    lobster.style.backgroundColor = "white";
};

lobster.addEventListener("mouseover", showLobster);
lobster.addEventListener("mouseout", hideLobster);

